#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dst_s)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (dst_s == 0)
		return (src_len);
	if (dst_len >= dst_s)
		return (dst_s + src_len);
	i = 0;
	while (src[i] != '\0' && dst_len + i < dst_s - 1)
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return (dst_len + src_len);
}
